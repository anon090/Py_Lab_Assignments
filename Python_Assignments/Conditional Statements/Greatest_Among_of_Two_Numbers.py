#Program to find the greatest among two numbers

# Input: Prompt user to enter two numbers
num1=int(input("Enter First Number:"))
num2=int(input("Enter Second Number:"))

# Comparison: Determine which number is greater
if num1 > num2 :
    print(f"The number {num1} is Greatest")
else:
    print(f"The number {num2} is Greatest")
