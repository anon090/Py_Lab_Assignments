# Program to calculate the area of a rectangle.

# Get the length of the rectangle from user input.
length=float(input("Enter length of rectangle: "))

# Get the breadth of the rectangle from user input.
breadth=float(input("Enter breadth of rectangle: "))

# Calculate the area using the formula: area = length * breadth
area=length*breadth

# Output the result formatted to two decimal places.
print("Area of Rectangle : %.2f"%area)
